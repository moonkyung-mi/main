package com.spring_boot_react.project.codey;

public class codey {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println("덧셈: " + (a + b));    // 덧셈 결과
        System.out.println("뺄셈: " + (a - b));    // 뺄셈 결과
        System.out.println("곱셈: " + (a * b));    // 곱셈 결과
        System.out.println("나눗셈: " + (a / b));  // 나눗셈 결과 (몫)
        System.out.println("나머지: " + (a % b));  // 나눗셈의 나머지
    }
}
